<h1 style="color: #0a7ff4; text-align: center;">Сопровождение процедуры банкротства</h1>

<p><img src="../pictures/soprovojdenie prozeduri bankrotstva.jpg" border="0" alt="Сопровождение процедуры банкротства" title="Сопровождение процедуры банкротства" width="250" height="159" style="float: left; margin-top: 4px; margin-bottom: 4px; margin-left: 7px; margin-right: 7px;"/>Согласно законодательству, фактическая ликвидация фирмы расценивается как добровольный процесс ликвидации, однако нередко в этот момент у ликвидируемых юридических лиц обнаруживается непогашенная задолженность.</p>

<p> Возникшая ситуация может расцениваться двояко: в процессе ликвидации предприятия оно должно полностью рассчитаться с кредиторами, но для этого не хватит размера стоимости всего имущества данного предприятия. </p>


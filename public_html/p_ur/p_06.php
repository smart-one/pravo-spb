    <p>Возврат  долгов может осуществляться по различным основаниям: долговым распискам,  договорам займа, неисполненным договорам поставки или кредитным договорам и  т.п.</p>
    <p>Возврат  долгов производится в соответствии с действующим законодательством путем  получения необходимого судебного решения. </p>
    <p>Наша  юридическая компания оказывает полный комплекс услуг по судебному взысканию  долгов, включающий в себя:<br>
      - проведение предварительной правовой экспертизы документации по данной  задолженности, оценка перспектив судебного взыскания; <br>
      - составление искового заявления и подача его в суд;<br>
      - участие в судебных заседаниях, поддержание и обоснование позиции по делу;  контроль за движением дела в суде;<br>
      - представительство в суде апелляционной (кассационной, надзорной) инстанции;<br>
      - получение исполнительных документов на решение суда о взыскании долга. <br>
      <br>
      Дополнительно,  мы оказываем услугу по сопровождению исполнительного производства в службе  судебных приставов.</p>
    <p>С Вас  лишь требуется выдать доверенность на ведение дела в суде - все остальное мы  сделаем сами &ndash; Ваше присутствие в суде не потребуется. </p>
    <p><i>Базовая стоимость услуги по  возврату долга составляет 10&nbsp;000 рублей + 5% от суммы долга.</i><br>
      <i>&nbsp;Стоимость услуги </i><i>зависит от ряда причин (суммы долга, причин не возврата долга, документов,  подтверждающих возникновение долга и иных факторов), определяется индивидуально  в каждом конкретном случае и может меняться как в большую так и в меньшую  сторону. </i></p>

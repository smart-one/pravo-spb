<h1>Возврат долгов</h1>
<p><strong>Возврат долгов </strong>- одна  из наиболее острых проблем сегодняшнего дня, поэтому важно получить быструю и  квалифицированную юридическую помощь, для того, чтобы защитить свои интересы.</p>
<p>Многие  стремятся самостоятельно решить проблему возврата долгов, однако чаще всего это  ухудшает и без того непростую ситуацию. Возврат долгов &ndash; это процесс, который  требует отличного знания и строгого соблюдения законов.</p>
<p>Центр  Правовых Услуг&nbsp; предлагает <b><u>комплексную услугу  юридическим и физическим лицам по возврату долгов</u> </b> и взысканию  всех видов дебиторских задолженностей.</p>
<p>Ваше  дело будут вести юристы, имеющие опыт более 15 лет именно в данной сфере.
Сотрудники Центра правовых услуг проведут тщательный анализ юридического 
состояния должника, особенности и специфику его деятельности, 
оценят сроки и характер задолженности. 
На основе полученных сведений будет разработана 
индивидуальная схема возврата долга.

</p>
<p>Стоимость  услуги по взысканию долгов составляет 10&nbsp;000 рублей + 5% от суммы долга <br>
  В  зависимости от сложности дела и суммы долга, возможна индивидуальная  договоренность.<br>
